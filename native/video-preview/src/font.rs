#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn dimensions_constant() {
        assert_eq!(GLYPH_W, 5);
        assert_eq!(GLYPH_H, 7);
    }

    #[test]
    fn space_is_blank_known_chars_nonblank() {
        assert_eq!(glyph(' '), [0; 7]);
        assert_ne!(glyph('A'), [0; 7]);
        assert_ne!(glyph('0'), [0; 7]);
        assert_ne!(glyph(':'), [0; 7]);
    }
}
