//! A tiny built-in 5x7 bitmap font for the forensic lower-third.
//!
//! WHY a bitmap font, not glyphon/wgpu_text: those mature glyph crates currently
//! pin to an OLDER wgpu (0.11 -> wgpu 26), which would conflict with this sidecar's
//! wgpu 29 and start a dependency-resolution fight. A self-contained bitmap font
//! gives real, legible ASCII text on the overlay with ZERO version coupling and no
//! extra deps. Documented follow-up (build-notes.md): swap to glyphon once it
//! tracks wgpu 29 for anti-aliased, proportional glyphs.
//!
//! Each glyph is 5 wide x 7 tall, encoded as 7 rows of 5 low bits (bit 4 = left).
//! Covers the characters a forensic timecode + caption needs.

pub const GLYPH_W: usize = 5;
pub const GLYPH_H: usize = 7;

